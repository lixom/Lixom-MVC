<h2>Installation</h2>
<p>First you have to make the data-directory writable. This is the place where Lixom needs
to be able to write and create files.</p>
<blockquote>
<code>cd lixom; chmod 777 site/data</code>
</blockquote>

<p>Second, Lixom has some modules that need to be initialised. You can do this through a 
controller. Point your browser to the following link.</p>
<blockquote>
<a href='<?=create_url('module/install')?>'>module/install</a>
</blockquote>